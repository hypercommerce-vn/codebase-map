# Learner display order and labels
_LEARNER_SECTIONS = [
    ("naming", "Naming Conventions"),
    ("layers", "Layer Architecture"),
    ("ownership", "Code Ownership & Bus Factor"),
]


def generate_patterns_md(
    vault: "CodebaseVault",
    confidence_threshold: float = 60.0,
    output_path: Optional[str] = None,
) -> str:
    """Generate a human-readable patterns.md from vault committed patterns.

    Args:
        vault: CodebaseVault with committed patterns.
        confidence_threshold: Minimum confidence to include (default 60%).
        output_path: If provided, write to this path. Otherwise write to
            vault's default location.

    Returns:
        The generated Markdown string.
    """
    all_patterns = vault.query_patterns()

    # Filter by confidence
    patterns = [p for p in all_patterns if p.confidence >= confidence_threshold]

    # Group by category
    by_category: Dict[str, List[Pattern]] = {}
    for p in patterns:
        by_category.setdefault(p.category, []).append(p)

    # Sort each group by confidence descending
    for cat in by_category:
        by_category[cat].sort(key=lambda p: p.confidence, reverse=True)

    # Build Markdown
    lines: List[str] = []
    ts = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    lines.append("# Codebase Patterns")
    lines.append("")
    lines.append(f"> Generated: {ts}")
    lines.append("> Vertical: codebase")
    lines.append(
        f"> Patterns: {len(patterns)} " f"(threshold: {confidence_threshold}%)"
    )
    lines.append("")
    lines.append("---")
    lines.append("")

    # Emit sections in defined order
    emitted_categories: set = set()
    for category, title in _LEARNER_SECTIONS:
        cat_patterns = by_category.get(category, [])
        if not cat_patterns:
            continue
        emitted_categories.add(category)
        lines.extend(_render_section(title, cat_patterns))

    # Emit any remaining categories not in the predefined order
    for category, cat_patterns in by_category.items():
        if category not in emitted_categories and cat_patterns:
            title = category.replace("_", " ").title()
            lines.extend(_render_section(title, cat_patterns))

    # Footer
    lines.append("---")
    lines.append("")
    lines.append(
        f"*{len(patterns)} patterns discovered · "
        f"Confidence threshold: {confidence_threshold}% · "
        f"Generated by Codebase Memory*"
    )
    lines.append("")

    content = "\n".join(lines)

    # Write to file if vault is initialized
    if output_path:
        with open(output_path, "w", encoding="utf-8") as f:
            f.write(content)
    elif vault.vault_dir:
        vert_dir = vault.vault_dir / "verticals" / "codebase"
        vert_dir.mkdir(parents=True, exist_ok=True)
        dest = vert_dir / "patterns.md"
        dest.write_text(content, encoding="utf-8")

    return content


def _render_section(title: str, patterns: List[Pattern]) -> List[str]:
    """Render a section of patterns as Markdown lines."""
    lines: List[str] = []
    lines.append(f"## {title}")
    lines.append("")

    for p in patterns:
        lines.extend(_render_pattern(p))

    return lines


